// int a = 10;

// var a: number = 10;

// syntax
// var <variable-name>: <data-type> = <value>;
var a: number;
a = 10;
console.log('a =', a);

a = 200;
console.log('a =', a);

// ES6 / ES2015 - EcmaScript - specs
let b: string = 'Hello TypeScript programmer';
console.log('b =', b);

b = 'Hi';
console.log('b =', b);

const c: string = "Constant string, cannot be changed.";
console.log('c =', c);

c = "abc";
console.log('c =', c);

